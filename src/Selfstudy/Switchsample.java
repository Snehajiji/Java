package Selfstudy;

public class Switchsample {

	public static void main(String[] args) 
	{
		int num=2;
		
		switch(num+2)
		{
		case 1:
			System.out.println(num);
			
		case 2:
			System.out.println(num);
			
		case 3:
			System.out.println(num);
			
		case 4:
			System.out.println(num);
			break;
			
		default:
			System.out.println("INVALID");
			
		
		}

	}

}
