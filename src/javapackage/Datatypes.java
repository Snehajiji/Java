package javapackage;

public class Datatypes {

	public static void main(String[] args) 
	
	//Primitive datatypes
	{
		byte b=125;
		System.out.println("Value of b = "+b);
		
		short s=325;
		System.out.println("Value of s= "+s);
		
		int a=1101,b1=543,c;
		c=a+b1;
		System.out.println("Value of c= "+c);
		
		long l1=55443327l;
		System.out.println("Vlue of L1= "+l1);
		
		float f1=11.45f,f2=23.012f,f3;
		f3=f1*f2;
		System.out.println("Value of f3= "+f3);
		
		double d1=11.454567,d2=23.012234567,d3;
		d3=d1-d2;
		System.out.println("Value of d3= "+d3);
		
		boolean s1=25>10;
		System.out.println("Value of s1 is "+s1);
		
		char t='S';
		System.out.println("Value in t is "+t);
		
		String s2="Sneha";
		System.out.println("Value of s2 is "+s2);
		
		
		
		
		

	}

}
